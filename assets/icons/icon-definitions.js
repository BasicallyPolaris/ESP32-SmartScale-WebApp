// Function to create an inline SVG icon
function createIcon(iconName, className = "icon") {
  const iconPath = iconDefinitions[iconName];
  if (!iconPath) {
    console.warn(`Icon "${iconName}" not found`);
    return `<div class="${className}">?</div>`;
  }

  return `<svg class="${className}" viewBox="0 0 24 24">${iconPath}</svg>`;
}

// Function to replace i tags and div icons with inline SVGs
function replaceIconTags() {
  const iconElements = document.querySelectorAll(
    'i[class*="icon-"], div[class*="icon-"]'
  );

  iconElements.forEach((element) => {
    const classes = element.className.split(" ");
    const iconClass = classes.find((cls) => cls.startsWith("icon-"));

    if (iconClass) {
      const iconName = iconClass.replace("icon-", "");
      const otherClasses = classes
        .filter((cls) => !cls.startsWith("icon-"))
        .join(" ");
      const finalClass = otherClasses ? `icon ${otherClasses}` : "icon";

      const svgElement = document.createElement("div");
      svgElement.innerHTML = createIcon(iconName, finalClass);

      // Replace the element with the SVG
      element.parentNode.replaceChild(svgElement.firstChild, element);
    }
  });
}

